

function sum(a,b) {
    var sum = a+b;
    return sum;
}

function difference (a,b) {
    var difference = a-b;
    return difference;
}

function quotient(a,b) {
    var  quotient = a/b;
    return quotient;
}

function power(a,b) {
    var power = a/b;
    return power;
}

module.exports = {
    sum: sum,
    difference: difference
  };